#1) Variable can contain letters,num & _
#2) Do not start with num
#3) Do not use Spaces (for spaces in var name use '_')
#4) Do not use function keywords(e.g: type,for) 
  #( Google python keywords so that we cannot use in variable names )
#5) short & descriptive names
#6) Case Sensitive (ADVICE: use lowercase for our comfort)
  
